export const getAllProducts = () => {
  return Object.values(productCategories).flatMap(category => category.products);
};

export const getProductsByCategory = (categoryId) => {
  return productCategories[categoryId]?.products || [];
};

export const getBestSellers = () => {
  return getAllProducts().filter(product => product.isBestseller);
};

export const getNewArrivals = () => {
  return getAllProducts().filter(product => product.isNew);
};

export const getFeaturedProducts = (limit = 8) => {
  const products = getAllProducts();

  return products
    .filter(product => product.isBestseller || product.rating >= 4.6)
    .slice(0, limit);
};

export const getProductById = (productId) => {
  return getAllProducts().find(product => product.id === productId);
};

export const searchProducts = (query) => {
  const searchTerm = query.toLowerCase();
  return getAllProducts().filter(product => 
    product.name.toLowerCase().includes(searchTerm) ||
    product.description.toLowerCase().includes(searchTerm) ||
    product.tags.some(tag => tag.includes(searchTerm))
  );
};